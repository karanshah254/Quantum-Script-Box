// logical AND operator 
let firstName = 'David';
let age = 20;
if (firstName === 'David' && age >= 18) {
    console.log('correct boy');
} else {
    console.log('incorrect boy');
}

// logical OR operator
let myAge = 18;
if (myAge < 5 || myAge > 15) {
    console.log('valid');
}
else {
    console.log('invalid');
}

